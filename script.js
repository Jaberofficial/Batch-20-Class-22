

function sum(data1, data2) {

    let number1 = data1;
    let number2 = data2;

    let sum = number1 + number2;

    return sum;
}

let sumResult =
    sum(40, 20);
console.log(sumResult);

console.log(sum(40, 80));


function sub(data1, data2) {

    let number1 = data1;
    let number2 = data2;

    let sub = number1 - number2;

    return sub;
}

console.log(sub(90, 80));
